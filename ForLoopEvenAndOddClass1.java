package javahomeworkpakage;

public class ForLoopClass1 {
    public static void main(String[] args) {

        System.out.println(" Odd Numbers");
        for(int i = 1; i <= 10; i++)
        {
            System.out.println(2 * i - 1);
        }

        System.out.println("Even Numbers");
        for(int i = 1; i <= 10; i++)
        {
            System.out.println(2 * i);
        }
    }
}

