public class PostiveAndNegativeAndZeroClass3 {

    public static void main(String[] args) {
        int num = -65;
        if (num > 0) {
            System.out.println("Number is Positive.");
        } else if (num < 0) {
            System.out.println("Number is Negative.");
        } else {
            System.out.println("Number is zero.");
        }
    }

}



