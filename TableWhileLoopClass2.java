package javahomeworkpakage;

public class TableWhileLoopClass2 {
    public static void main(String[] args) {
        int number = 9;

        System.out.println("Multiplication Table of 9:");

        for (int i = 1; i <= 10; i++) {
            int result = number * i;
            System.out.println(number + " x " + i + " = " + result);
        }
    }
}

